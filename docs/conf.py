# Resolve to GitHub URLs.
def linkcode_resolve(domain: str, info: dict[str, Any]) -> Optional[str]:
  if domain != 'py':
    return None
  elif 'module' not in info:
    return None
  filename = info['module'].replace('.', '/')
  return f'https://github.com/apparebit/deface/{filename}.py'


